/**
 * 
 * @param {String} name name of the person
 * @returns greeting for the person
 */
export const sayHello = (name) => {
    return `Hello ${name}`;
}

/**
 * 
 * @param {Number} a first number
 * @param {Number} b second number
 * @returns addition of two numbers
 */
export const add = (a, b) => {
    return a + b;
}
